# neurotron.ansi.py: define ANSI color sequences

class Ansi:
    R = '\x1b[31m'        # red
    G = '\x1b[32m'        # green
    Y = '\x1b[33m'        # yellow
    B = '\x1b[34m'        # blue
    M = '\x1b[35m'        # magenta
    C = '\x1b[36m'        # cyan
    N = '\x1b[0m'         # neutral
